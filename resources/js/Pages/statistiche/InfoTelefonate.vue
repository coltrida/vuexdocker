<template>
    <div>
        <v-row class="mt-5">
            <v-col cols="6">
                <h2>Telefonate: {{infoRecalls.mese}} / {{infoRecalls.anno}}</h2>
            </v-col>

            <v-col cols="6" class="flex justify-end">
                <v-btn color="primary" dark @click="cancella">
                    Chiudi
                </v-btn>
            </v-col>
        </v-row>

        <div class="text-center" v-if="carica">
            <v-progress-circular
                indeterminate
                color="primary"
            ></v-progress-circular>
        </div>
        <div v-else>
            <v-row>
                <v-col cols="12">
                    <v-data-table
                        dense
                        item-key="idTelefonata"
                        :headers="headers"
                        :items="getTelefonateAnnoMese"
                        class="elevation-1 mt-3"
                    >
                        <template v-slot:item.nominativo="{ item }">
                            <router-link style="color: black" :to="{ name: 'clientsFiliale',
                                        params: { filialeId: item.filiale_id, nomRicerca:item.nome, cogRicerca:item.cognome + ' ' + item.nome, }}">
                                {{item.cognome + ' ' + item.nome}}
                            </router-link>
                        </template>

                        <template v-slot:item.recalls="{ item }">
                            <div v-for="telefonata in item.recalls" :key="telefonata.id">
                                {{telefonata.datarecall}}
                            </div>
                        </template>

                        <template v-slot:item.esito="{ item }">
                            <div v-for="telefonata in item.recalls" :key="telefonata.id">
                                {{telefonata.esito}}
                            </div>
                        </template>
                    </v-data-table>
                </v-col>
            </v-row>
        </div>

    </div>

</template>

<script>
    import {mapActions, mapGetters} from "vuex";
    export default {
        name: "InfoTelefonate",

        props: ['infoRecalls'],

        data(){
            return {
                carica: false,
                headers: [
                    { text: 'Data', width:150, value: 'recalls', class: "indigo white--text" },
                    { text: 'Nominativo', width:220, value: 'nominativo', class: "indigo white--text" },
                    { text: 'CittÃ ', width:220, value: 'citta', class: "indigo white--text" },
                    { text: 'Esito', width:220, value: 'esito', class: "indigo white--text" },
                ],

            }
        },

        mounted() {
            this.carica = true;
            this.fetchTelefonateAnnoMese({
                    'anno':this.infoRecalls.anno,
                    'mesenumero':this.infoRecalls.mesenumero,
                    'idTelefonante':this.infoRecalls.idTelefonante
            })
                .then(() => this.carica = false);
        },

        methods: {
            ...mapActions('telefonate', {
                fetchTelefonateAnnoMese:'fetchTelefonateAnnoMese',
            }),

            cancella(){
                this.$emit('chiudiInfo')
            },
        },

        computed:{
            ...mapGetters('telefonate', {
                getTelefonateAnnoMese:'getTelefonateAnnoMese',
            }),
        }
    }
</script>

